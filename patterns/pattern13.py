n = int(input("Enter the value: "))

x = n
for i in range(0,n):
    print("*"*(n-i) + " "*i*2 + "*"*(x))
    x -= 1
y =1
for j in range(0,n):
    print("*"*(j+1) + " "*(n-1-j)*2 + "*"*y)
    y += 1


